require "TankZed_Util"
require "lua_timers"
TankZedModII = TankZedModII or {}

TankZedModII.shouldCrash = true

function TankZedModII.getCar()
	local car = nil;
	local pl = getPlayer();
	if pl:getVehicle() then
		car = pl:getVehicle();
	end
	return car
end

function TankZedModII.doCrashReact(zed)
	--zed:setVariable("hitreaction", "CarCollide")
	TankZedModII.shouldCrash = false
end

function TankZedModII.CarCrash(zed)
	if not zed then return end
	local CarHit = SandboxVars.TankZedModII.CarHit or true

	if CarHit then return end
	if TankZedModII.isTankZed(zed) then
		local pl = getPlayer()
		if not pl then return end
		if not (pl:isAlive() and zed:isAlive()) then return end

		if TankZedModII.shouldCrash == true then
			local car = TankZedModII.getCar()
			if car then
				if car:isDriver(pl) and car:isCollided(zed) then
					if car:isEngineRunning() and not car:isDestroyed() then
						car:crash(ZombRand(80, 101), true)
						ISVehicleMenu.onShutOff(pl)
						--ISVehicleDashboard.damageFlick(pl)
						TankZedModII.doCrashReact(zed)

						if isClient() then
							sendClientCommand('TankZedII', 'Crash', {zedID = zed:getOnlineID()})
						end

					end
				end
			end
		end
		if SandboxVars.TankZedModII.CarCrawl then
			if zed:isUnderVehicle() then
				if not zed:isCrawling() then
					zed:toggleCrawling()
					--zed:setCanWalk(false)
					zed:setCanCrawlUnderVehicle(true)
				end
			end
		end
	end
end

Events.OnZombieUpdate.Remove(TankZedModII.CarCrash)
Events.OnZombieUpdate.Add(TankZedModII.CarCrash)

Events.EveryTenMinutes.Add(function() TankZedModII.shouldCrash = true end)
Events.OnExitVehicle.Add(function() TankZedModII.shouldCrash = true end)
Events.OnEnterVehicle.Add(function() TankZedModII.shouldCrash = true end)




