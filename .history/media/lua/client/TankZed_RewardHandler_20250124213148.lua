function TankZedModII.parseItems(num)
    local tab = {}
    local DropList = TankZedModII.getDeathRewards(num, "DropList")

    for item in string.gmatch(DropList, "([^;]+)") do
        table.insert(tab, item)
    end
    return tab
end

